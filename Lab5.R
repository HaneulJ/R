# 문제3
# 함수 exam3( )을 생성한다.
# 매개변수 : 2 개, 리턴 값 :  1개
# 기능 : 전달받은 2개의 데이터 중에서 큰 값에서 작은 값의 차를 리턴, 두 값이 동일하면 0 을 리턴한다.

# 결과 출력은 함수를 호출한 다음 리턴 값을 받아서 호출한 쪽에서 한다.

exam3 <- function(n1,n2){
  if (n1 <= n2){
    result <- n2 - n1
  }
  else{
    result <- n1 - n2}
  return (result)}

exam3(5,6)
exam3(7,3)
exam3(4,4)


# 문제4
# 함수 exam4( )를 생성한다.
# 매개변수 : 3 개, 리턴 값 :  1개
# 기능 : 아규먼트를 숫자 연산자 숫자 순으로 전달받는다. (연산자는 +, -, *, %/%, %% 를 받는 것으로 정한다)
# 전달된 두 개의 숫자에 대하여 연산을 처리하고 그 결과를 리턴한다.
# 단,다른 연산자가 전달되면 "규격의 연산자만 전달하세요"를 리턴한다.
# %/% 와 %%  가 전달된 경우에 한해서 첫 번째 숫자가 0이면 "오류1" 이라고 리턴한다.
# %/% 와 %%  가 전달된 경우에 한해서 두 번째 숫자가 0이면 "오류2" 라고 리턴한다.
# 함수를 호출하여 리턴된 결과를 출력하는 것은 호출한 쪽에서 한다.

exam4 <- function(n1,c,n2){
  result <- 0
  if(c == "+"){
    result <- n1 + n2
  }else if(c == "-"){
    if(n1 >= n2){
      result <- n1 - n2
    }
    else{
      result <- n2 - n1
    }
  }else if(c == "*"){
    result <- n1 * n2
    }else if(c == "%/%" | c == "%%"){
    if(n1 == 0){
      result <-"오류1"
    }
    else if(n2 ==0) {
      result <-"오류2"
    }else{
      if(c=="%/%"){
      result <- n1 %/% n2}
      else{
        result <- n1 %% n2
      }}
  }else{
    return("규격의 연산자만 전달하세요")
  }
  return(result)
}

exam4(2,"+",4)
exam4(5,"-",3)
exam4(3,"*",3)
exam4(8,"%/%",3)
exam4(0,"%/%",3)
exam4(8,"%%",3)
exam4(8,"%%",0)
exam4(1,"=",6)



