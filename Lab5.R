# 문제6
# 함수 exam6( )를 생성한다.
# 매개변수 : 1 개, 리턴 값 :  없음(NULL 리턴)
# 기능 : 아규먼트로 전달되는 벡터에는 학생들의 점수(0~100)가 들어 있다.점수에 따라서 결과를 출력한다.
# 85~100 "상", 70~84  "중", ~69    "하"
# 출력형식 : "xx 점은 x등급입니다." 
# NA 값이 존재하는 경우엔 "NA 는 처리불가" 를 출력한다.
# 모든 출력은 print() 함수를 사용한다.

exam6 <- function(x){
  for(i in 1:length(x)){
  if(is.na(x[i]))
    print("NA는 처리불가") # na에 대해 먼저 정의해야 오류 안뜸
  else if(x[i] >= 85)
    print(paste(x[i],"점은 상등급입니다.")) # print함수는 하나의 식만 출력 가능
  else if(x[i] >= 70)
    print(paste(x[i],"점은 중등급입니다."))
  else
    print(paste(x[i],"점은 하등급입니다."))
 }
}


exam6(NA)
exam6(80)
exam6(50)
exam6(70)
exam6(96)
exam6(35)
exam6(c(80, 50, 70, 66, NA, 35))